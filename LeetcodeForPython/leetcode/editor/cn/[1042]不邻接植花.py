class Solution:
    def gardenNoAdj(self, n: int, paths: List[List[int]]) -> List[int]:
        if not paths: return [1] * n
        res = [0] * n  # res[i]è¡¨ç¤ºç¬¬i+1ä¸ªèŠ±å›­çš„èŠ±ç§ç±»
        G = [[] for _ in range(n)]  # å£°æ˜é‚»æ¥è¡¨
        # æ„å»ºé‚»æ¥è¡¨
        for x, y in paths:
            G[x - 1].append(y - 1)
            G[y - 1].append(x - 1)

        for i in range(n):
            # éå†å›¾ä¸ºæ¯ä¸ªèŠ‚ç‚¹ä¸Šè‰²
            # å½“å‰èŠ‚ç‚¹içš„é‚»æ¥èŠ‚ç‚¹çš„é¢œè‰²é›†åˆã€‚ï¼ˆä¿è¯ièŠ‚ç‚¹çš„é¢œè‰²ä¸é‚»æ¥èŠ‚ç‚¹é¢œè‰²ä¸åŒï¼‰
            colors = {res[j] for j in G[i]}
            res[i] = ({1, 2, 3, 4} - colors).pop()

        return res


